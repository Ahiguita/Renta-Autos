    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            Connection conect = DriverManager.getConnection(url, "sa", "z");
            PreparedStatement pst = conect.prepareStatement("{call RegistrarConductor (?,?,?,?,?,?,?)}");
            pst.setInt(1, Integer.parseInt(this.txtCedConductor.getText()));
            pst.setString(2, this.txtNomConductor.getText());
            pst.setString(3, this.txtApeConductor.getText());
            pst.setString(4, this.txtDireConductor.getText());
            pst.setInt(5, Integer.parseInt(this.txtTeleConductor.getText()));
            pst.setString(6, this.txtLiceConductor.getText());
            pst.setString(7, this.cbxCategoria.getSelectedItem().toString());


            ResultSet rs = pst.executeQuery();
            String resultado = "";
            while (rs.next()) {
                resultado = rs.getString(1);
            }
            JOptionPane.showMessageDialog(null, resultado);
            conect.close();
            Limpiar();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "No se pudo conectar" + e);
        }

    

    }//GEN-LAST:event_btnGuardarActionPerformed

