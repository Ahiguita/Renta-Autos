    private void txtCedConductorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCedConductorKeyPressed
         if (evt.getKeyCode()==10) {
              try {
                String dni = this.txtCedConductor.getText();
                Connection con = DriverManager.getConnection(url, "sa", "z");
                PreparedStatement pst = con.prepareStatement("BuscarConductor '" + dni + "'," + null);
                ResultSet rs = pst.executeQuery();
                rs.next();
                this.txtNomConductor.setText(rs.getString(2));
                this.txtApeConductor.setText(rs.getString(3));
                this.txtDireConductor.setText(rs.getString(4));
                this.txtTeleConductor.setText(rs.getString(5));
                this.txtLiceConductor.setText(rs.getString(6));
                //int tarj=rs.getInt(5);
                this.cbxCategoria.setSelectedItem(rs.getString(7));
                con.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error en la conexion" + e);
            }
            evt.consume();
         }
    }//GEN-LAST:event_txtCedConductorKeyPressed

